package New;

public class Animal {
	
	private String nome;
	private String movimentoEvasivo;
	private int idade;

	public String getNome() {
		return nome;
	}

	public void setNome(final String nome) {
		this.nome = nome;
	}

	public String getMovimentoEvasivo() {
		return movimentoEvasivo;
	}

	public void setMovimentoEvasivo(final String movimentoEvasivo) {
		this.movimentoEvasivo = movimentoEvasivo;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(final int idade) {
		this.idade = idade;
	}
	
}
